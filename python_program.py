def calculate_fee(days):
    if days <= 5:
        fee = days * 2
    elif 6 <= days <= 10:
        fee = (5 * 2) + (days - 5) * 3
    elif 11 <= days <= 15:
        fee = (5 * 2) + (5 * 3) + (days - 10) * 4
    else:
        fee = (5 * 2) + (5 * 3) + (5 * 4) + (days - 15) * 5
    return fee

days = int(input("Enter the number of days the book was used: "))


if days > 0:
    total_fee = calculate_fee(days)
    print(f"The total fee for {days} days is: Rs. {total_fee}")
else:
    print("Please enter a positive number of days.")